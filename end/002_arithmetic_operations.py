# Bu örneklerde python'da aritmetik operasyonlar nasıl yapılır onu öğrenelim.

a = 13
b = 2

# Toplama

son = a + b
print(f'Toplama işleminin sonucu: {son}')

# Çıkarma
son = a - b
print(f'Çıkarma işleminin sonucu: {son}')

# Çarpma
son = a * b
print(f'Çarpma işleminin sonucu: {son}')

# Bölme
son = a / b
print(f'Bölme işleminin sonucu: {son}')

# Üstel
son = a ** b
print(f'Üstel işlemin sonucu: {son}')

# Mod bulma
son = a % b
print(f'Mod bulma işleminin sonucu: {son}')